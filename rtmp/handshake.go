//Handshake rtmp 简单握手流程
func Handshake(brw *bufio.ReadWriter) error {

	buf := ReadBuf(brw, 1537)

	// C0+C1->
	if buf[0] != Version {
		return errors.New("rtmp version is not right")
	}

	// <-S0+S1+S2
	SREG := buf[0:]
	for _, value := range buf[1:1537] {
		SREG = append(SREG, value)
	}

	brw.Write(SREG)
	brw.Flush()

	// C2->
	ReadBuf(brw, 1536)
	return nil
}
